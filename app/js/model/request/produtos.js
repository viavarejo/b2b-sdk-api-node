export class Produtos {
    getCodigo() {
        return this.codigo;
    }
    setCodigo(codigo) {
        this.codigo = codigo;
    }
    getQuantidade() {
        return this.quantidade;
    }
    setQuantidade(quantidade) {
        this.quantidade = quantidade;
    }
    getIdLojista() {
        return this.idLojista;
    }
    setIdLojista(idLojista) {
        this.idLojista = idLojista;
    }
}
