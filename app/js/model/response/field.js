export class Field {
    getField() {
        return this.field;
    }
    setField(field) {
        this.field = field;
    }
    getValue() {
        return this.value;
    }
    setValue(value) {
        this.value = value;
    }
    getMessage() {
        return this.message;
    }
    setMessage(message) {
        this.message = message;
    }
}
