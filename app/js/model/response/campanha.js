export class Campanha {
    getIdCampanha() {
        return this.idCampanha;
    }
    setIdCampanha(idCampanha) {
        this.idCampanha = idCampanha;
    }
    getNome() {
        return this.nome;
    }
    setNome(nome) {
        this.nome = nome;
    }
    getDataInicio() {
        return this.dataInicio;
    }
    setDataInicio(dataInicio) {
        this.dataInicio = dataInicio;
    }
    getDataFim() {
        return this.dataFim;
    }
    setDataFim(dataFim) {
        this.dataFim = dataFim;
    }
    getIdTipoCampanha() {
        return this.idTipoCampanha;
    }
    setIdTipoCampanha(idTipoCampanha) {
        this.idTipoCampanha = idTipoCampanha;
    }
    getTipoCampanha() {
        return this.tipoCampanha;
    }
    setTipoCampanha(tipoCampanha) {
        this.tipoCampanha = tipoCampanha;
    }
    getCnpjContrato() {
        return this.cnpjContrato;
    }
    setCnpjContrato(cnpjContrato) {
        this.cnpjContrato = cnpjContrato;
    }
    isStatus() {
        return this.status;
    }
    setStatus(status) {
        this.status = status;
    }
    getEntregaTipos() {
        return this.entregaTipos;
    }
    setEntregaTipos(entregaTipos) {
        this.entregaTipos = entregaTipos;
    }
}
