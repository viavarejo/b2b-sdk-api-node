export class Motivo {
    getCategoria() {
        return this.categoria;
    }
    setCategoria(categoria) {
        this.categoria = categoria;
    }
    getAssunto() {
        return this.assunto;
    }
    setAssunto(assunto) {
        this.assunto = assunto;
    }
    getMotivo() {
        return this.motivo;
    }
    setMotivo(motivo) {
        this.motivo = motivo;
    }
    getObservacao() {
        return this.observacao;
    }
    setObservacao(observacao) {
        this.observacao = observacao;
    }
}
