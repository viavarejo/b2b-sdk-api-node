export class EntregaTipo {
    getIdEntregaTipo() {
        return this.idEntregaTipo;
    }
    setIdEntregaTipo(idEntregaTipo) {
        this.idEntregaTipo = idEntregaTipo;
    }
    getNome() {
        return this.nome;
    }
    setNome(nome) {
        this.nome = nome;
    }
    isHabilitado() {
        return this.habilitado;
    }
    setHabilitado(habilitado) {
        this.habilitado = habilitado;
    }
}
